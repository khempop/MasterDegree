public class company {

	public String name = "";
	public int volume;
	public int price;
	public int change;
	public int day;
	
	public void setValume(int vol){
		volume = vol;
	}
	
	public int getVolume(){
		return volume;
	}
	
	public void setName(String n){
		name = n;
	}
	
	public String getName(){
		return name;
	}
	
	public void setPrice(int p){
		price = p;
	}
	
	public int getPrice(){
		return price;
	}
	
	public void setChange(int chg){
		change = chg;
	}
	
	public int getChange(){
		return change;
	}
	
	public void setDay(int d){
		day = d;
	}
	
	public int getDay(){
		return day;
	}

}